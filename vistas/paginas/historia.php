          <h2>El vino en occidente</h2>
          <p class="texto-historia">El consumo de vino se extendió hacia el occidente, llegando a Anatolia y Grecia; y hacia el sur, llegando hasta Egipto, ya célebre en Bahariya durante el Imperio Medio (siglo XX a. C.).
          <br>
                La más antigua documentación griega sobre el cuidado de la vid, la cosecha y el prensado de las uvas, es Los trabajos y los días, de Hesíodo, del siglo VIII a. C. En la antigua Grecia el vino se bebía mezclado con agua y se conservaba en pellejos de cabra.
          <br>
                La viticultura debe su mayor desarrollo a la propagación del cristianismo, por ser el vino necesario para la celebración de la misa. Los monasterios, con sus propios métodos de elaboración y extracción, fueron los precursores de la viticultura y vinicultura, dejando huellas tan claras como los vinos priorato, proveniente de la palabra prior.
          </p>
