          <h2>Primeros hallazgos</h2>
          <p class="texto-historia">El vino se produjo por primera vez durante el neolítico, según los testimonios arqueológicos hallados en los montes Zagros, en la región que hoy ocupan Irak e Irán, gracias a la presencia de Vitis vinifera sylvestris y la aparición de la cerámica durante este periodo. La evidencia más antigua de la producción y consumo de vino es una vasija del año 5400 a. C., hallada en el poblado neolítico de Hajii Firuz Tepe, en los montes Zagros.
          <br>
                La vasija contiene un residuo rojizo, presumiblemente vino. Aunque recientemente se ha encontrado la bodega más antigua conocida, datada en el año 6000 a. C., que sitúa en Armenia la producción más antigua de vino.</p>
        </div>           
