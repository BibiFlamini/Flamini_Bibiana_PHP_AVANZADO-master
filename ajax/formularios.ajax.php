            <h2 class="post-title">Fue falsificada una de las etiquetas de vino más famosa del mundo</h2>
            <div class="post-contenido text-justify">
              <p> El delito más común vinculado al vino es el fraude de etiquetas, y sobre todo de marcas de gran valor en el mercado internacional. La última noticia vinculada a este tema, se sitúa en la provincia china de Fujian, donde se pudo allanar una red de falsificación masiva. Allí se descubrieron más de 40.000 botellas de vino con etiquetas falsas, así como equipos de envasado y etiquetado. Se estimo que el valor del mercado de estos vinos falsos, cuando fueran vendidos es de más de $150 millones.</p>
              <p>El operativo fue realizado por la Oficina de Supervisión del Mercado de Fujian, con las oficinas locales de seguridad pública, como parte de su campaña de "Puño de Hierro" contra alimentos y bebidas falsificados e ilegales. Las autoridades revelaron que entre los productos falsificados incautados había botellas falsificadas de Château Lafite Rothschild de Burdeos y Penfolds de Australia; ambas marcas son enormemente populares en China.</p>
