            <h2 class="post-title">El duro y sacrificado trabajo de los viñateros</h2>
            <div class="post-contenido text-justify"> 
              <p>El pasado 1 de febrero se conmemoró el Día del Trabajador Vitivinícola, con el objetivo de reconocer a las personas que trabajan en la elaboración del vino. Paradójicamente, el día lunes trabajadores vitivinícolas autoconvocados se movilizaron por el centro de Mendoza para reclamar por mejores condiciones de trabajo y un salario digno. </p>
              <p>Una persona que trabaja en la viña comienza su labor a las 6 de la mañana, para aprovechar las horas de menor temperatura. El trabajador que está en la poda, sufre mucho dolor de hueso en las manos y quien cosecha, en ocasiones sufre deshidrataciones severas, además, sus manos sufren el contacto constante con la totora.</p>
              <p>Con respecto a la cosecha, anteriormente era habitual utilizar tachos de 22 kilos, actualmente se cosecha en caja o directamente dentro de los bins. Un cosechador trabaja entre 10 a 12 horas con un promedio de 70 o 60 tachos, dependiendo de la cepa que tenga cada uva. </p>
              <p>Respecto al trabajo infantil, se encuentra casi totalmente erradicado, en épocas pasadas eran los niños quienes levantaban los granos del suelo.</p>
